<script>
function datatables() {

    var table = $('#table').DataTable({
        dom: 'RBfrtip',
        buttons: [],
        select: true,
        
        aaSorting     : [[0, 'asc']],
        iDisplayLength: 25,
        stateSave     : true,
        responsive    : true,
        fixedHeader   : true,
        processing    : true,
        serverSide    : true,
        "bDestroy"    : true,
        pagingType    : "full_numbers",
        ajax          : {
            url     : '{{ url('admin/print_buttons/ajax/data_user') }}',
            dataType: 'json'
        },
        columns       : [
            {data: 'name', name: 'name'},
            {data: 'preview_button', name: 'preview_button'},
            {data: 'print_button_status', name: 'print_button_status'}
        ],
    });
}

datatables();

function funChangeStatus(id,status) {
    $("#pageloader").fadeIn();
    $.ajax({
      url : '{{ route('admin.print_buttons.ajax.change_status_user') }}',
      data: {
        "_token": "{{ csrf_token() }}",
        "id": id,
        "status": status
        },
      type: 'get',
      dataType: 'json',
      success: function( result )
      {
        datatables();
        $("#pageloader").hide();
      }
    });
}
